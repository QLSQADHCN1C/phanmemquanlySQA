    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
        // TODO add your handling code here:
        Connection cn = getConnect();
        String ID = txtTK.getText().trim();
        Sqlx="select * from HANGHOA where MAHH=?";
        String Colums[]={"MAHH","TENHH","GIABAN","DONVITINH","MANCC"};
        DefaultTableModel Dtl = new DefaultTableModel(Colums,0);
        try {
            Pstt=cn.prepareStatement(Sqlx);
            Pstt.setString(1, ID);
            Rlsr=Pstt.executeQuery();
            while(Rlsr.next())
            {
                Vector Vts=new Vector();
                Vts.add(Rlsr.getString("MAHH"));
                Vts.add(Rlsr.getString("TENHH"));
                Vts.add(Rlsr.getString("GIABAN"));
                Vts.add(Rlsr.getString("DONVITINH"));
                Vts.add(Rlsr.getString("DONVITINH"));
                Dtl.addRow(Vts);

            }
            tblTrangChu.setModel(Dtl);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnTKActionPerformed

